package operadores

fun main(){
    // Tabela- verdade
    // AND
    println(true && true)
    println(true && false)
    println(false && true)
    println(false && false)

    //OR
    println(true || true)
    println(true || false)
    println(false || true)
    println(false || false)

    //NOT
    println("NOT F ${!false}")
    println("NOT T ${!true}")

}